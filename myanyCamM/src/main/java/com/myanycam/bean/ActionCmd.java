package com.myanycam.bean;


public class ActionCmd {
	private String xns;
	private String cmd;
	private String[] appends;

	public String getXns() {
		return xns;
	}

	public void setXns(String xns) {
		this.xns = xns;
	}

	public String getCmd() {
		return cmd;
	}

	public void setCmd(String cmd) {
		this.cmd = cmd;
	}

	public String[] getAppends() {
		return appends;
	}

	public void setAppends(String[] appends) {
		this.appends = appends;
	}
	
	
}
